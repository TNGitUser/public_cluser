double	ft_fmod(double a, double b)
{
	double m;

	if (a == 0 || b == 0)
		return (0);
	if (a < 0)
		m = a * -1.;
	else
		m = a;
	if (b < 0)
		b *= -1;
	while (m >= b)
		m -= b;
	if (a < 0)
		m *= -1;
	return (m);
}
